 private void srediFormu() {
         centrirajFormu();
        BukingDruga k = (BukingDruga) Kontroler.vratiInstancuKontrolera().getMapa().get("bukingdruga_izabran");
        prikaziBuking(k);
    }

    private void centrirajFormu() {
        setLocationRelativeTo(null);
    }

    private void prikaziBuking(BukingDruga k) {
        jTxtAgencijaID.setEditable(false);
         jTxtAgencijaID.setText(k.getAgencijaID()+"");
        jTxtKontakt.setText(k.getKontaktTel());
         jTxtEmail.setText(k.getEmail());
         jTxtAdresa.setText(k.getAdresa());
     
    }

    private void osvezi() {
         if (fm != null) {
            fm.osvezi();
        }
    }




