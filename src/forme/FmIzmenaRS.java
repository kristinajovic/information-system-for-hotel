        RezervisaneSobe re=new RezervisaneSobe();
                re.setRezervacija(r);
        re.setSobaID(sobID);
        re.setBrojOdraslih(brOd);
        re.setBrojDece(brDec);
        re.setIznos(iznos);
       // re.setNapomena(naziv);
        re.setBrojSobe(brSo);
        try {
            Kontroler.vratiInstancuKontrolera().izmeniRezervisanuSobu(re);
            JOptionPane.showMessageDialog(this, "Rezervisana soba je uspesno izmenjena");
            
               osvezi();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
