        int sobID = Integer.parseInt(jTxtSobaID.getText().trim());

         int brRez = Integer.parseInt(jTxtBrRez.getText().trim());
       
      
        
        int brOd = Integer.parseInt(jTxtBrOdraslih.getText().trim());
