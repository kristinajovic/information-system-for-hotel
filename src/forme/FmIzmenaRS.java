 private void srediFormu() {
         centrirajFormu();
        RezervisaneSobe k = (RezervisaneSobe) Kontroler.vratiInstancuKontrolera().getMapa().get("rezsoba_izabran");
        prikaziRezSobu(k);
    }

    private void centrirajFormu() {
        setLocationRelativeTo(null);
    }

    private void prikaziRezSobu(RezervisaneSobe k) {
       // jcmbBrojRezervacije.setSelectedItem(k.getRezervacija());
      jTxtBrRez.setText(k.getRezervacija().getBrojRezervacije()+"");
      jTxtSobaID.setText(k.getSobaID()+ "");
     jTxtBrOdraslih.setText(k.getBrojOdraslih()+ "");
     jTxtBrDece.setText(k.getBrojDece()+ "");
     jTxtIznos.setText(k.getIznos()+ "");
     jTxtNapomena.setText(k.getNapomena());
     jTxtBrSobe.setText(k.getBrojSobe()+"");
       
    }

    private void osvezi() {
         if (rezSobe != null) {
            rezSobe.osvezi();
        }
    }

