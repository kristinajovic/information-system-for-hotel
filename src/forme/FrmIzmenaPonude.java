    private void jTxtRestoranIDKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTxtRestoranIDKeyPressed
        int pon = Integer.parseInt(jTxtPonudaID.getText().trim());
         String naziv = jTxtNazivPonude.getText();

         int zah = Integer.parseInt(jTxtZahtevID.getText().trim());
       
         int res= Integer.parseInt(jTxtRestoranID.getText().trim());
      
        
        ZahtevPonuda z=new ZahtevPonuda();
        z.setZahtevID(zah);

        Restoran re=new Restoran();
      //  RezervisaneSobe k = new RezervisaneSobe(r,sobID,brOd,brDec,iznos,naziv,brSo);

        re.setRestoranID(res);
        PonudaRest r=new PonudaRest();
        r.setPonudaID(pon);
       
        r.setNazivPonude(naziv);
        r.setZ(z);
        r.setR(re);
       // jButton3.setVisible(false);
        jTxtRestoranID.setEditable(false);
        try {
            Kontroler.vratiInstancuKontrolera().izmeniPonudu2(r);
            JOptionPane.showMessageDialog(this, "Rezervisana soba je uspesno izmenjena");
            
               osvezi();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_jTxtRestoranIDKeyPressed

