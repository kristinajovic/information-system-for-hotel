 private void srediFormu() {
         centrirajFormu();
        PonudaRest k = (PonudaRest) Kontroler.vratiInstancuKontrolera().getMapa().get("ponuda_izabrano");
        prikaziPonudu(k);
    }

    private void centrirajFormu() {
        setLocationRelativeTo(null);
    }

    private void prikaziPonudu(PonudaRest k) {
        jTxtPonudaID.setText(k.getPonudaID()+"");
        jTxtNazivPonude.setText(k.getNazivPonude());
        jTxtZahtevID.setText(k.getZ().getZahtevID()+"");
        jTxtRestoranID.setText(k.getR().getRestoranID()+"");
      
    }

    private void osvezi() {
         if (ponuda != null) {
            ponuda.osvezi();
        }
    }

