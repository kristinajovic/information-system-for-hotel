 private void srediFormu() {
         centrirajFormu();
        Buking k = (Buking) Kontroler.vratiInstancuKontrolera().getMapa().get("bukingview_izabran");
        prikaziBuking(k);
    }

    private void centrirajFormu() {
        setLocationRelativeTo(null);
    }

    private void prikaziBuking(Buking k) {
        jTxtAgencijaID.setEditable(false);
         jTxtAgencijaID.setText(k.getAgencijaID()+"");
        jTxtNaziv.setText(k.getNazivAgencije());
         jTxtKontakt.setText(k.getKontaktTel());
         jTxtEmail.setText(k.getEmail());
         jTxtAdresa.setText(k.getAdresa());
     
    }

    private void osvezi() {
         if (fm != null) {
            fm.osvezi();
        }
    }


