    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String naz = jTxtNaziv.getText();
        String kon = jTxtKontakt.getText();
        String em = jTxtEmail.getText();
        String ad = jTxtAdresa.getText();

        int id=Integer.parseInt(jTxtAgencijaID.getText().trim());
        Buking k = new Buking();
        k.setAgencijaID(id);
         k.setNazivAgencije(naz);
        k.setKontaktTel(kon);
        k.setEmail(em);
        k.setAdresa(ad);

        try {
            Kontroler.vratiInstancuKontrolera().izmeniBukingView(k);
            JOptionPane.showMessageDialog(this, "Agencija je uspesno izmenjena");
            osvezi();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
            
           
        }
    }//GEN-LAST:event_jButton1ActionPerformed

