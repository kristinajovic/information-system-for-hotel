    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        FakturaFizickoLice r = new FakturaFizickoLice();
        
        int brr=Integer.parseInt(jTxtBrRacuna.getText().trim());

        r.setBrojRacuna(brr);
        
        
        int rbr = Integer.parseInt(jTxtRbrStavke.getText().trim());

        int kol = Integer.parseInt(jTxtKolicina.getText().trim());
        
        
        JedinicaMere jm=new JedinicaMere();
        int jmid=Integer.parseInt(jTxtJedMereID.getText().trim());
        jm.setJmId(jmid);
        
        Usluga us=new Usluga();
        
        int usid=Integer.parseInt(jTxtUsluga.getText().trim());
        
        us.setUslugaId(usid);
        
        StavkaFakture k = new StavkaFakture(rbr,r,kol,jm,us);

        try {
            Kontroler.vratiInstancuKontrolera().izmeniStavkuFakture(k);
            JOptionPane.showMessageDialog(this, "Stavka je uspesno izmenjena");
            //   osvezi();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

