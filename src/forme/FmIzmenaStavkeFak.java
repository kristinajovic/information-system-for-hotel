     private void srediFormu() {
         centrirajFormu();
        StavkaFakture k = (StavkaFakture) Kontroler.vratiInstancuKontrolera().getMapa().get("stavka_izabran");
        prikaziStavku(k);
    }

    private void centrirajFormu() {
        setLocationRelativeTo(null);
    }

    private void prikaziStavku(StavkaFakture k) {
      jTxtBrRacuna.setText(k.getFaktura().getBrojRacuna()+"");
      jTxtRbrStavke.setText(k.getRbrStavke()+ "");
     jTxtKolicina.setText(k.getKolicina()+ "");
     jTxtUsluga.setText(k.getUsluga().getUslugaId()+ "");
    
     jTxtJedMereID.setText(k.getJedinicaMere().getJmId()+"");
       
    }

    private void osvezi() {
         if (stavka != null) {
            stavka.osvezi();
        }
    }

